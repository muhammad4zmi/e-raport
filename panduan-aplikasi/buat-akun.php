<h1><i class="fa fa-pencil-square-o fa-fw fa-lg"></i>Membuat Akun Baru</h1>
<p>
    Ketika Anda mengetikkan alamat "eraport.portofolioku.web.id" pada address bar browser Anda, maka akan tampil Halaman Utama dari Aplikasi
    yang memunculkan halaman login user. <br>
    ketikkan Username dan Password anda ketika <a href="#daftar" title="Cara Daftar"><span class="label label-warning">Mendaftar.</span></a>
</p>
<img src="buat-akun/1.JPG" alt="..." class="img-thumbnail img-responsive">
<p class="text-center">Halaman Utama (Login)</p>
<br/>

<p>
    <a name="daftar"></a>
    Untuk membuat akun baru, Inputkan Dahulu NIM Anda, kemudian klik tombol hijau (panah). maka akan muncul data Nama dan Email Anda pada daftar
    isian. Kemudian isikan password baru Anda.
</p>
<img src="buat-akun/2.JPG" alt="..." class="img-thumbnail img-responsive">
<p class="text-center">Halaman Utama (Buat Akun)</p>

<br/>

<p>
    Setelah membuat Akun, buka situs <a href="https://portofolioku.web.id:2096" target="_blank"><span class="label label-warning"><big>https://portofolioku.web.id:2096</big></span></a> untuk membuka Email dari Sistem yang
    berisi link untuk melakukan aktivasi akun Anda. <i>Jika Anda mengalami kesulitan di dalam proses login ke akun email Anda, Segera Hubungi pihak 
       Tata Usaha untuk konsultasi username atau password.</i>
</p>
<img src="buat-akun/3.JPG" alt="..." class="img-thumbnail img-responsive">
<p class="text-center">Form Login pada situs Web Mail E-Raport</p>

<br/>

<p>
    Setelah Anda Berhasil Login ke Akun email. Silahkan pilih Mail User Agent Client yg akan digunakan untuk membuka kotak masuk email Anda.
    Disini kami contohkan jika menggunakan Mail User Agent dari <span class="label label-success">RoundCube</span>.<br/>
    Klik <span class="label label-warning"><big>Icon RoundCube</big></span> atau link <span class="label label-warning"><big>"Read Mail using RoundCube"</big></span> untuk membuka email Anda.
</p>
<img src="buat-akun/4.JPG" alt="..." class="img-thumbnail img-responsive">
